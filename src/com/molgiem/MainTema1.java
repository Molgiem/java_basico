package com.molgiem;

public class MainTema1 {

    public static void main(String[] args) {
        int numeroInt = 42;
        long numeroLong = 123456789123L;
        double numeroDouble = 42.42d;
        boolean bool = true;
        String datoString = "Hola mundo";

        System.out.println("Ejemplos de datos primitivos:");
        System.out.println("Integer: " + numeroInt);
        System.out.println("Long: " + numeroLong);
        System.out.println("Double: " + numeroDouble);
        System.out.println("Booleano: " + bool);
        System.out.println("String: " + datoString);
    }

}
